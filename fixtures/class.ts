export class registrationForm {
  public firstName(first: any) {
    return first;
  }

  public lastName(last: any) {
    return last;
  }

  public patronymic(patronymic: any) {
    return patronymic;
  }

  public email(email: any) {
    return email;
  }

  public phoneNumber(number: any) {
    return number;
  }

  public password(password: any) {
    return password;
  }

  public age(age: any) {
    return age;
  }
}
